package io.sanket8081.LinkedList.Node;

public class CircularNode {

	int value;
	CircularNode next;
	
	public String toString()
	{
		return value+"";
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public CircularNode getNext() {
		return next;
	}

	public void setNext(CircularNode next) {
		this.next = next;
	}
	
	
	
	
}
