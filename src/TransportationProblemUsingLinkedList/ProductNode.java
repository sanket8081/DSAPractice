package TransportationProblemUsingLinkedList;

public class ProductNode {

	String status;
	int time;
	ProductNode next;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public ProductNode getNext() {
		return next;
	}
	public void setNext(ProductNode next) {
		this.next = next;
	}
}
